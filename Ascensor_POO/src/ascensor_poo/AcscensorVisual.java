    public void CrearAcesnsor() {

        if (A1 == null && c1 == null) {
            c1 = new Cabina(Integer.parseInt(this.txttamaño.getText()));
            A1 = new Acscensor(c1, 15, Integer.parseInt(this.txtpersonas.getText()));
        } else {
            c1.setTamañoX(Integer.parseInt(this.txttamaño.getText()));
            c1.setTamañoY(Integer.parseInt(this.txttamaño.getText()));
            A1.setCabina(c1);
            A1.setNroDePersonas(Integer.parseInt(this.txtpersonas.getText()));

        }
    }

    public void Subiendo() {

        new Thread(() -> {
            try {
                txtestado.setText("Subiendo.");
                Thread.sleep(250);

                txtestado.setText("Subiendo..");
                Thread.sleep(250);

                txtestado.setText("Subiendo...");
                Thread.sleep(250);

                txtestado.setText("Subiendo....");
                Thread.sleep(250);

                txtestado.setText("!Listo!");
            } catch (InterruptedException e) {
                e.printStackTrace();
            }
        }).start();

    }

