        CrearAcesnsor();

        if (this.txtpiso.getText().equals("PB")) {

            A1.Subir(2);
            if (A1.ComprobacionDeCapacidad() == false) {
                this.txtestado.setText("!ERROR!");
                this.txtpiso.setText(A1.Imprimir());
            } else {
                Subiendo();
                this.txtpiso.setText(A1.Imprimir());
            }

        } else if (Integer.parseInt(this.txtpiso.getText()) > 2) {
            for (int i = Integer.parseInt(this.txtpiso.getText()); i >= 0; i--) {
                A1.Bajar();
                this.txtpiso.setText(i + "");
            }
        }

